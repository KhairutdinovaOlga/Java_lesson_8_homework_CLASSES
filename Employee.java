//Создать класс Employee с полями: имя, возраст, пол и ЗП в день.
public class Employee {
    String name;
    int age;
    String gender;
    double salaryPerDay;

    public Employee(String name, int age, String gender, double salaryPerDay) {
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.salaryPerDay= salaryPerDay;
}
}

